    private void saveFileMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFileMenuItemActionPerformed
        // TODO add your handling code here:
        jFileChooser2.setCurrentDirectory(defaultDir);
        int result = jFileChooser2.showSaveDialog(saveFileMenuItem);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser2.getSelectedFile();
            String filePath = file.getAbsolutePath();
            try(BufferedWriter writer = new BufferedWriter(new FileWriter(filePath, true))) {
                for(int i = 0; i < 63; i++){
                    writer.write(memory[i]+"\n");
                }
                writer.write(memory[63]);
                writer.flush();
            } catch (IOException e) {
                Logger.getLogger(CacheGUI.class.getName()).log(Level.SEVERE, null, e);
            }
        }       
    }//GEN-LAST:event_saveFileMenuItemActionPerformed

