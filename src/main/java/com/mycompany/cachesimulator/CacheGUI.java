    private void openFileMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileMenuItemActionPerformed
        // TODO add your handling code here:        
        int result = jFileChooser1.showOpenDialog(openFileMenuItem);
        File file = jFileChooser1.getSelectedFile();
        
        if (result == JFileChooser.APPROVE_OPTION) {
            try{
                boolean validFile = true;
                ArrayList<String> tmp = openFile(file);
                if(tmp.size()!= 64){
                    validFile = false;
                }else{
                    for(int i=0; i<64; i++){
                        if(!validData(tmp.get(i))){
                            validFile = false;
                            break;
                        }
                    }
                }
                if(validFile){
                    javax.swing.JTextField[] data = {
                        data0, data1, data2, data3, data4, data5, data6, data7, 
                        data8, data9, data10, data11, data12, data13, data14, data15, 
                        data16, data17, data18, data19, data20, data21, data22, data23, 
                        data24, data25, data26, data27, data28, data29, data30, data31, 
                        data32, data33, data34, data35, data36, data37, data38, data39, 
                        data40, data41, data42, data43, data44, data45, data46, data47, 
                        data48, data49, data50, data51, data52, data53, data54, data55, 
                        data56, data57, data58, data59, data60, data61, data62, data63};
                    for(int i=0; i<64; i++){
                        memory[i]=tmp.get(i);
                        data[i].setText(memory[i]);
                    }
                }else{
                    fileError.setVisible(true);
                }
            }catch(FileNotFoundException e){          
                //Logger.getLogger(CacheGUI.class.getName()).log(Level.SEVERE, null, ex);
                fileNF.setVisible(true);
            }
        }
    }//GEN-LAST:event_openFileMenuItemActionPerformed

