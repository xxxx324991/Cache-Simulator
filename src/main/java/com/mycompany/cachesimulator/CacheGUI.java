    private void cacheButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cacheButtonActionPerformed
        // TODO add your handling code here:
        javax.swing.JTextField cacheTag[] = {tag0, tag1, tag2, tag3, tag4, tag5, tag6, tag7};
        javax.swing.JTextField cacheData[] = {dat0, dat1, dat2, dat3, dat4, dat5, dat6, dat7};
        String addrStr = addrTextField.getText();
        
        if(validAddr(addrStr)){
            int addr = parseInt(addrStr);
            addrTextField.setText("");
            lastCachedTextField.setText(Integer.toString(addr));
            int set = addr % 4;
            int tag = addr / 4;
            CacheComponent tmp = new CacheComponent(tag, memory[addr]);
            if(tmp.equals(cache[set][0]) || tmp.equals(cache[set][1])){
                hitQTextField.setText(hit);
            }else{
                hitQTextField.setText(miss);
                if(tmp.getTag().equals(cache[set][0].getTag())){
                    cache[set][0].setData(tmp.getData());
                    cacheData[2*set].setText(tmp.getData());
                }else if(tmp.getTag().equals(cache[set][1].getTag())){
                    cache[set][1].setData(tmp.getData());
                    cacheData[2*set+1].setText(tmp.getData());
                }else{
                    cache[set][setPos[set]].setTag(tmp.getTag());
                    cache[set][setPos[set]].setData(tmp.getData());
                    cacheTag[2*set+setPos[set]].setText(tmp.getTag());
                    cacheData[2*set+setPos[set]].setText(tmp.getData());
                    setPos[set] = (setPos[set] + 1) % 2;
                }
            }
        }else{
            addrError.setVisible(true);
        }       
    }//GEN-LAST:event_cacheButtonActionPerformed

